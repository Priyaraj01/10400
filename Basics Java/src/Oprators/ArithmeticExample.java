package Oprators;

public class ArithmeticExample {

	public static void main(String[] args) {
		
		int A=5;
		int B=10;
		int C=A+B;
		System.out.println("Add="+C);
		int D=A*B;
		System.out.println("Mul="+D);
		int E=B-A;
		System.out.println("Sub="+E);
		int F=A/B;
		System.out.println("Div="+F);

	}

}
